$resultadosub = $subtracao1 - $subtracao2;

echo "O valor $subtracao1 menos o valor $subtracao2 é igual $resultadosub";
echo "<br><hr>";

echo "Operador de Divisão <br>";

$divisao1 = 100;
$divisao2 = 20;

$resultadodivisao = $divisao1 / $divisao2;

echo "Os valores $divisao1 e $divisao2, dividos, resulta em: $resultadodivisao";
echo "<br><hr>";

echo "Operador de Multiplicação <br>";

$multiplicacao1 = 4;
