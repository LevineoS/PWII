 * Operadores:
 * (+) = adição;
 * (-) = subtração;
 * (/) = divisão;
 * (*) = multiplicação;
 * (%) = resto da divisão;
 */

echo "Operador de Adição <br>";

$valor1 = 71;
$valor2 = 52;

$resultado = $valor1 + $valor2;

echo "$valor1 + $valor2 = $resultado";
echo "<br><hr>";

echo "Operador de Subtração <br>";

$subtracao1 = 41;
